package softwarecoding003;

public class SoftwareCoding003_practice001 {

	public static void main(String[] args) {
		// #1. 연산 후 변수에 대입하기
		int kopo34_ii; // ii라는 int형 변수를 선언한다.
		kopo34_ii = 1 + 2; // 변수 ii에 1+2를 대입한다.
		System.out.printf("#1-1 : %d\n", kopo34_ii); // 변수 ii를 프린트한다.
		kopo34_ii = 1 + 2 * 3; // 변수 ii에 1+2*3을 대입한다.
		System.out.printf("#1-2 : %d\n", kopo34_ii); // 변수 ii를 프린트한다.
	}
}
